def გააორმაგე_რიცხვები_ვაილით(რიცხვები):
    ინდექსი = 0
    ახალი_სია = []
    while ინდექსი < len(რიცხვები):
        ახალი_სია.append(რიცხვები[ინდექსი] * 2)
        ინდექსი += 1
    return ახალი_სია

print(გააორმაგე_რიცხვები_ვაილით([1, 2, 3])) 

