def გაამრავლე_ჯამები(სია1, სია2):
    ჯამი1 = 0
    ჯამი2 = 0

    for რიცხვი in სია1:
        ჯამი1 += რიცხვი
    for რიცხვი in სია2:
        ჯამი2 += რიცხვი

    return ჯამი1 * ჯამი2

print(გაამრავლე_ჯამები([1, 2, 3], [4, 5]))

