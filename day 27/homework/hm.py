def მხოლოდ_ლუწები(რიცხვები):
    ლუწები = []
    for რიცხვი in რიცხვები:
        if რიცხვი % 2 == 0:
            ლუწები.append(რიცხვი)
    return ლუწები

print(მხოლოდ_ლუწები([1, 2, 3, 4, 5, 6]))

